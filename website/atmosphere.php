Non-cryogenic air separation processes are most likely to be a suitable and cost effective choice when high purity product is not required and/or when the required production rate is relatively small. 
<br><br>Non-cryogenic processes use physical properties other than boiling point to separate and purify components of air at close-to-ambient temperature.  Systems belong to one of two major technology categories: adsorption processes and membrane diffusion-separation systems.  Adsorption-based processes may be described using a number of generic names  (Pressure Swing Adsorption or PSA, Vacuum Swing Adsorption or VSA, Vacuum-Pressure Swing Adsorption or VPSA) or by trade names. The same holds true for Membrane separation systems. 
<br><br>PSA, VSA and VPSA systems use differences in adsorption of gases on specially-fabricated materials to make the desired separations.  Different adsorbents are used for oxygen and nitrogen generation, but the physical appearance and operating principles of the systems are similar. 
<br><br>Membrane systems use differences in diffusion rates between, for instance, oxygen and nitrogen or hydrogen and CO2 through the walls of specially designed and fabricated hollow polymer tubes. 
